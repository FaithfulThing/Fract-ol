#include"libft.h"

int	ft_ptr_len(unsigned long long int n)
{
	int			count;

	count = 1;
	if (n < 0)
		n *= -1;
	while (n / 16 > 0)
	{
		n /= 16;
		count++;
	}
	return (count);
}
